# Write a python code to find out occurrence of an elements in a list.

lst = ['a','e','i','o','u','a','e','i','o','a','e']
print("Number of times a occurs : ",lst.count("a"))
print("Number of times e occurs : ",lst.count("e"))
print("Number of times i occurs : ",lst.count("i"))
print("Number of times o occurs : ",lst.count("o"))
print("Number of times u occurs : ",lst.count("u"))